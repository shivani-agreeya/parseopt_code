      case "QCOMM2" =>
        qcomm2TableStruct
      case "B192" =>
        b192TableStruct
      case "B193" =>
        b193TableStruct
      case "ASN" =>
        asnTableStruct
      case "NAS" =>
        nasTableStruct
      case "IP" =>
        ipTableStruct
      case "QCOMM5G" =>
        qcomm5GTableStruct
      case "HIVE" =>
        finalHiveTableStruct
    }

