  }
  def deleteHivePartitionIfExists(spark:SparkSession,fileName:String,hiveTblName:String)={
    if(spark.catalog.tableExists("dmat_logs.finallogrecord_dmat"+hiveTblName)){
      spark.sql("ALTER TABLE dmat_logs.finallogrecord_dmat" + hiveTblName + " DROP IF EXISTS PARTITION (fileName='" + fileName + "')")
    }
